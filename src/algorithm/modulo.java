public class Modulo {

    public static void main(String args[] ) throws Exception {
        System.out.println(2%3); //=0...2
        System.out.println(1%7);//=0...1
        System.out.println(7%1);//=7..0

        System.out.println(-2%3);//=0..-2
        System.out.println(-3%2);//=-1..-1

    }
}
